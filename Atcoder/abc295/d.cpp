using ll = long long int;

string s;
unordered_map<int, int> ump;

int main ( void ) {
    s = read_string ();
    int num = 0;
    ll ans = 0;
    ump[num]++;
    for ( auto c : s ) {
        num ^= ( 1 << int ( c - '0' ) );
        auto src = ump.find ( num );
        if ( src != ump.end () ) {
            ans += src->S;
        }
        ump[num]++;
    }
    printf ( "%lld\n", ans );

    return 0;
}
